#include<stdio.h>

int main() {
	int times = 0; //테스트 케이스
	int rank1 = 0;//순위 대회1
	int rank2 = 0;//순위 대회2
	int total_price = 0;//전체 상금
	int plus1 = 0;
	int x2 = 1;
	scanf("%d", &times);

	for (int i = 0; i < times; i++) {
		//테스트 케이스 만큼 rank1, rank2을 입력받는다.
		scanf("%d %d", &rank1, &rank2);

		////////////////////////////////////////////////////////////////////////////////////////////////

		//첫번째 대회 기준(등차수열)

		//실제등수를 상금등수로 환산하기 (1등은 1등으로 처리하고 안돌린다.)


		//n은 2등에서 6등까지를 뜻한다. n=1 일때가 성립하면 2등이라는 뜻
		if (rank1 > 1 && rank1<22) {
			for (int n = 1; n < 6; n++) {
				plus1 += 1;
				//등차수열의 합을 이용함(gsat에서 배운 거 활용)
				//n(n+1)/2 + 1 <= rank <= n(n+1)/2 + n(등수)+1 이용  ==> n+1등
				if (((n * (n + 1))/2+1 <= rank1)&&((n * (n + 1)/2 + plus1 + 1) >=rank1)) {
					//n = 3~5 즉 4등에서 6등 계산
					if (n <= 5 && n >= 3) {
						total_price += -20 * n + 110;
						break;
					}
					
					//n = 1~2 즉 2등에서 3등 계산
					else {
						total_price += 400 -100 * n;
						break;
					}
						
				}
			}
		}
		//등수가 1등일 때(대회1)
		if (rank1 == 1) {
			total_price += 500;
			
		}
		

		////////////////////////////////////////////////////////////////////////////////////////////////

		//두번째 대회 기준(등비수열)

		//실제등수를 상금등수로 환산하기 (1등은 1등으로 처리하고 안돌린다.)
		//n은 2등에서 5등까지를 뜻한다. n=1 일때가 성립하면 2등이라는 뜻
		if (rank2 > 1 && rank2<32) {
			for (int n = 1; n < 5; n++) {
				x2 *= 2;
				//n(n+1)/2 + 1 <= rank <= n(n+1)/2 + n(등수)+1 이용  ==> n+1등
				if ((x2 <= rank2) && (x2+(x2-1) >= rank2)) {
					total_price += 512 / x2;
					break;
				}
			}
		}
		//등수가 1등일 때(대회2)
		if (rank2 == 1) {
			total_price += 512;
		}

		//결과출력
		printf("%d\n", total_price*10000);

		//다음 연산을 위한 초기화 과정
		total_price = 0;
		plus1 = 0;
		x2 = 1;
	}

	
	return 0;
}